#include<stdio.h>
char a[6][9]={
'-','-','*','*','-','*','*','-','-',
'-','*','-','-','*','-','-','*','-',
'-','*','-','-','-','-','-','*','-',
'-','-','*','-','-','-','*','-','-',
'-','-','-','*','-','*','-','-','-',
'-','-','-','-','*','-','-','-','-'
};	
int main() {
	int n,m;
	scanf("%d%d",&n,&m);
	if(n<1||n>5||m<1||m>5){
		printf("Input Error\n");
		return 0;
	}
	for(int l=1;l<=n;l++){
		for(int i=0;i<6;i++) {
			for(int k = 1 ; k <= m ; ++k)
				for(int j = 0 ; j < 9 ; ++j)
					printf("%c",a[i][j]);
			        printf("\n");
		}
	}
}
